package variavbles;

public class Conversions {

	public static void main(String[] args) {
		
		int x = 10;
		double d = x;
		long l = x;
		float f = x;
		
		int y = (int) x;
		System.out.println(y);
		
		double dValue = 9.99;
		System.out.println(dValue);
		
		
		int iValue = (int) dValue;
		System.out.println(iValue);
	}

}
