package operators;

public class IfOperator {

	public static void main(String[] args) {
		
		int x = 10;
		
		if(x > 10){
			System.out.println("X is bigger than 10");
		} else if (x == 10) {
			System.out.println("X equals 10");
		} else {
			System.out.println("X is smaller than 10");
		}

	}

}
